Handlebars.registerHelper('formatTime', function(date) {
  var time = new Date(date*1000);

  var options = {
  year: 'numeric',
  month: 'numeric',
  day: 'numeric',
  hour: 'numeric',
  minute: 'numeric',
  second: 'numeric'
  };
  time = time.toLocaleString("ru", options);
  return time;
});

Handlebars.registerHelper('formatDate', function(ts) {
    return new Date(ts * 1000).toLocaleString();
});

